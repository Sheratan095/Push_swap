#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	char	check;

	check = (char)c;
	while (*str)
	{
		if (*str == check)
			return ((char *)str);
		str++;
	}
	if (*str == check)
		return ((char *)str);
	return (NULL);
}
